        public void playSound(String soundname)
        {
            
            foreach (var sound in audios)
            {
                if (sound.Key == soundname)
                {
                    instance = sound.Value.CreateInstance();
                }
            }
           
            instance.IsLooped = true;
            instance.Play();
        }

        public void stopSound()
        {
            if (instance != null)
            {
                instance.Stop();
            }
            
        }

